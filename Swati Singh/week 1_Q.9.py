# 9. Write a program in Python to add first 5 even numbers accepted through keyboard.
n1=int(input("Enter 1st even number: "))
n2=int(input("Enter 2nd even number: "))
n3=int(input("Enter 3rd even number: "))
n4=int(input("Enter 4th even number: "))
n5=int(input("Enter 5th even number: "))
sum=n1+n2+n3+n4+n5
print("Sum of 1st 5 even numbers= ",sum)
